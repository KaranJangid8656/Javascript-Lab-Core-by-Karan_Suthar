// Basic if else if else statement

let marks = 85;

if (marks >= 90 ) {
    console.log("You got A grade");
} else if (marks >= 80) {
    console.log("You got B grade");
} else if (marks >= 60) {
    console.log("You got C grade");
} else if (marks >= 40) {
    console.log("You got D grade");
} else {
    console.log("Fail");
}
