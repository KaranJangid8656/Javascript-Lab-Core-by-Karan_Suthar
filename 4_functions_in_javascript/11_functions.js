// Functions in javascript are reusable block of code designed to perform a specific task. 
// Functions can take input parameters and return outputs

// Example of a function

function greet(name){      // name is a parameter
    console.log("Hello " + name);

}

greet("Karan");   // "Karan" is the argument

// Parameter - name ( placeholder inside the function)
// Argument - "Karan" (Real Value given at call time)


